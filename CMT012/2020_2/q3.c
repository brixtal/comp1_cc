#include <stdio.h>

int main(void){

    int arbitros, i, j;
    float notas[1024], temp, media=0.0, qtd_notas_validas;

    scanf("%d", &arbitros);

    for(i=0; i<arbitros; i++){
        scanf("%f", &notas[i]);
    }

    for(i=0; i<arbitros-1; i++){
        for(j=0; j<arbitros-1-i; j++){
            if(notas[j]>notas[j+1]){
                temp = notas[j];
                notas[j] = notas[j+1];
                notas[j+1] = temp;
            }
        }
    }

    for(i=1; i<arbitros-1; i++){
        media+=notas[i];
    }
    qtd_notas_validas = ((float)(arbitros-2));
    media = media/qtd_notas_validas;

    printf("%3.1f\n", media);
    
    return 0;
}

