#include <stdio.h>

#define QTD_FILEIRAS 12
#define QTD_COLUNAS 12

int main(void){

    int cinema[QTD_FILEIRAS][QTD_COLUNAS], grupo, fileira, coluna, i, j, ocupados=0, livres_fileira=0;

    /* Inicializando mapa das poltronas. 0 marca que a poltrona está vazia*/
    for(i=0; i<QTD_FILEIRAS; i++){
        for(j=0; j<QTD_COLUNAS;j++){
            cinema[i][j] = 0;
        }
    }

    while(1){        
        scanf("%d-%d", &fileira, &coluna);
        
        if(coluna == 0 && fileira == 0){
            break;
        }

        cinema[fileira-1][coluna-1] = 1;
        ocupados++; /* Guarda quantas poltronas foram utilizadas */
    }
    
    scanf("%d", &grupo);

    for(i=0; i<QTD_FILEIRAS; i++){
        livres_fileira = 0;
        for(j=0; j<QTD_COLUNAS; j++){
            if(cinema[i][j] == 0){
                livres_fileira++; /* Marco que existe mais um lugar livre */
            } else {
                livres_fileira = 0;
            }
            
            if(livres_fileira == grupo){
                printf("O grupo pode se sentar juntos, a comecar pela poltrona %d-%d.\n", i+1, j-grupo+2);
                return 0; /*Encerro o programa porque já sei que é possível que o grupo sente junto. */
            }
        }
    }

    /* Daqui para baixo só é executado se o grupo não puder sentar junto */
    
    if(QTD_FILEIRAS*QTD_COLUNAS - ocupados >= grupo){
        printf("O grupo pode assistir a sessao, mas em lugares separados.\n");
    }
    else {
        printf("O grupo nao conseguira assistir o filme nesta sessao.\n");        
    }
    
    return 0;
}

